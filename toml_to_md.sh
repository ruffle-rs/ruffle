reset_dependency_state() {
    current_name=""
    repo=""
    license=""
    notices=()
    authors=()
    inside_authors=0
    inside_notices=0
}

parse_string_field() {
    local line=$1
    local key=$2
    if [[ "$line" =~ ^$key\ *=\ *\"(.*)\" ]]; then
        echo "${BASH_REMATCH[1]}"
    fi
}

parse_inline_array() {
    local content=$1
    local -n out_array=$2
    IFS=',' read -ra arr <<< "$content"
    for val in "${arr[@]}"; do
        out_array+=("$(echo "$val" | sed -E 's/^ *"//;s/" *$//')")
    done
}

parse_multiline_array_line() {
    local line=$1
    local -n out_array=$2
    local double_match=""
    local single_match=""

    if [[ "$line" =~ \"([^\"]+)\" ]]; then
        double_match="${BASH_REMATCH[1]}"
    fi

    if [[ "$line" =~ \'([^\']+)\' ]]; then
        single_match="${BASH_REMATCH[1]}"
    fi

    if [[ -n "$double_match" || -n "$single_match" ]]; then
        if (( ${#double_match} > ${#single_match} )); then
            out_array+=("$double_match")
        else
            out_array+=("$single_match")
        fi
    fi
}

format_license() {
    echo "$1" \
        | sed -E 's/ OR /\//g; s/ AND /,/g; s/\//\//g' \
        | sed -E 's/([A-Za-z0-9.+-]+)/[\1](#\1)/g' \
        | sed -E 's/\[WITH\]\(#WITH\)/WITH/g' \
        | sed -E 's/#([A-Za-z0-9.+-]+)/#\1/; s/#([A-Za-z0-9+-]*)\.([A-Za-z0-9+-]*)/#\1\2/g'
}

flush_dependency() {
    if [[ "$current_name" == nihav_core ]]; then
        link="[nihav-vp6](https://github.com/ruffle-rs/nihav-vp6)"
		license_md=$(format_license "MIT")
		if [[ ${#notices[@]} -gt 0 ]]; then
            notes=$(IFS="; "; echo "${notices[*]}")
        else
            notes=$(IFS="; "; echo "${authors[*]}")
        fi
		echo "| $link | $license_md | $notes |" >> "$OUTPUT_FILE"
	fi
    if [[ -n "$current_name" && -n "$repo" && "$current_name" != ruffle* && "$current_name" != h263-rs* && "$repo" != "https://github.com/ruffle-rs/ruffle" ]]; then
        for seen in "${seen_names[@]}"; do
            if [[ "$current_name" == "$seen" ]]; then
                return
            fi
        done
        link="[$current_name]($repo)"
        license_md=$(format_license "$license")
        if [[ ${#notices[@]} -gt 0 ]]; then
            notes=$(IFS="; "; echo "${notices[*]}")
        else
            notes=$(IFS="; "; echo "${authors[*]}")
        fi
        echo "| $link | $license_md | $notes |" >> "$OUTPUT_FILE"
        seen_names+=("$current_name")
    fi
}

append_license_files() {
    local dir=$1

    if [[ -d "$dir" ]]; then
        for file in "$dir"/*; do
            if [[ -f "$file" ]]; then
                name=$(basename "$file")
                echo "## $name" >> "$OUTPUT_FILE"
                echo '```' >> "$OUTPUT_FILE"
                cat "$file" >> "$OUTPUT_FILE"
                echo '```' >> "$OUTPUT_FILE"
                echo "" >> "$OUTPUT_FILE"
            fi
        done
    fi
}


handle_multiline_array() {
    local -n inside_flag="$1"
    local line="$2"
    local current_name="$3"
    local -n target_array="$4"

    if [[ $inside_flag -eq 1 && ! "$line" =~ ^\]$ && ! ( "$current_name" == "ruffle_web" && "$line" != *Ruffle* ) ]]; then
        parse_multiline_array_line "$line" "$4"
    else
        inside_flag=0
    fi
}

parse_array() {
    local line="$1"
    local key="$2"
    local -n array_ref="$3"
    local -n inside_flag="$4"

    if [[ "$line" =~ ^${key}\ *=\ *\[ ]]; then
        inside_flag=1
        array_ref=()
        if [[ "$line" =~ \[(.*)\] ]]; then
            parse_inline_array "${BASH_REMATCH[1]}" "$key"
            inside_flag=0
        fi
        return 0
    fi

    return 1
}

seen_names=()

while IFS= read -r line || [[ -n "$line" ]]; do
    stripped=$(echo "$line" | sed 's/^[[:space:]]*//')

    if [[ "$stripped" == "[[dependencies]]" ]]; then
        flush_dependency
        reset_dependency_state
        continue
    fi

    val=$(parse_string_field "$stripped" "name")
    [[ -n "$val" ]] && current_name="$val"

    val=$(parse_string_field "$stripped" "repository")
    [[ -n "$val" ]] && repo="$val"

    val=$(parse_string_field "$stripped" "license")
    [[ -n "$val" ]] && license="$val"

    if parse_array "$stripped" "notices" notices inside_notices; then
		continue
	fi

    if parse_array "$stripped" "authors" authors inside_authors; then
		continue
	fi
	handle_multiline_array inside_authors "$stripped" "$current_name" authors
    handle_multiline_array inside_notices "$stripped" "$current_name" notices
done < "$TOML_FILE"

flush_dependency

echo "" >> "$OUTPUT_FILE"
echo "# Licenses of Third-Party Libraries" >> "$OUTPUT_FILE"
echo "" >> "$OUTPUT_FILE"

append_license_files "$LICENSE_DIR"
append_license_files "$LICENSE_DIR/exceptions"

